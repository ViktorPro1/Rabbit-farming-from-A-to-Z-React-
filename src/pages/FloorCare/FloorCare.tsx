const FloorCare = () => {
  const [openId, setOpenId] = useState<string | null>(null);

  const toggle = (id: string) => {
    setOpenId(openId === id ? null : id);
  };

  return (
    <main className="floorcare-page">
      <div className="floorcare-header">
        <h1>Підлогове утримання кроликів</h1>
        <p>Групове утримання самок: організація, зонування, догляд</p>
      </div>

      <div className="floorcare-wrap">
        {sections.map((section) => (
          <div key={section.id} className="floorcare-section">
            <h2>{section.title}</h2>
            <p>{section.short}</p>
            <button
              className="floorcare-toggle-btn"
              onClick={() => toggle(section.id)}
            >
              {openId === section.id ? "Згорнути ▲" : "Детальніше ▼"}
            </button>
            {openId === section.id && (
              <div className="floorcare-detail">
                {typeof section.detail === "string" ? (
                  <p>{section.detail}</p>
                ) : (
                  section.detail
                )}
              </div>
            )}
          </div>
        ))}

        <div className="floorcare-back">
          <Link to="/" className="floorcare-back-btn">
            ⬅ На головну
          </Link>
        </div>
      </div>
    </main>
  );
};

export default FloorCare;
